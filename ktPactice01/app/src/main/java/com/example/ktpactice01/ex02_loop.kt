package com.example.ktpactice01

fun main()
{
    for( index in 1..10 ) {
        println(index)
    }
    println("-----------------------")
    for( index in 1..10 step 2) {
        println(index)
    }

    println("-----------------------")
    for(_i in 10 downTo 1 step 3) {
        println(_i)
    }




}
